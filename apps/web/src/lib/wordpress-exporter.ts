import "server-only";

import { mkdir, writeFile } from "fs/promises";
import { join } from "path";

import type { IntakeSelections } from "@/lib/types";

/**
 * Generate WordPress-ready export with theme files
 */
export async function generateWordPressExport(
  html: string,
  projectDir: string,
  intake: IntakeSelections
): Promise<void> {
  // Create WordPress theme structure
  const themeDir = join(projectDir, "wordpress-theme");
  await mkdir(themeDir, { recursive: true });

  // 1. Create functions.php
  const functionsPhp = generateFunctionsPHP(intake);
  await writeFile(join(themeDir, "functions.php"), functionsPhp);

  // 2. Create style.css
  const styleCss = generateStyleCSS(intake);
  await writeFile(join(themeDir, "style.css"), styleCss);

  // 3. Create index.php (main template)
  const indexPhp = generateIndexPHP(html, intake);
  await writeFile(join(themeDir, "index.php"), indexPhp);

  // 4. Create header.php
  const headerPhp = generateHeaderPHP();
  await writeFile(join(themeDir, "header.php"), headerPhp);

  // 5. Create footer.php
  const footerPhp = generateFooterPHP();
  await writeFile(join(themeDir, "footer.php"), footerPhp);

  // 6. Create README for installation
  const readme = generateWordPressReadme(intake);
  await writeFile(join(themeDir, "README.txt"), readme);
}

function generateFunctionsPHP(intake: IntakeSelections): string {
  return `<?php
/**
 * ${intake.companyName} Theme Functions
 * Auto-generated by ash-tra.com
 */

// Add theme support
add_theme_support('post-thumbnails');
add_theme_support('title-tag');
add_theme_support('custom-logo');
add_theme_support('customize-selective-refresh-widgets');

// Enqueue styles
function ${sanitizePhpFunction(intake.companyName)}_enqueue_styles() {
    wp_enqueue_style('${slugifyTheme(intake.companyName)}-style', get_stylesheet_uri());
    wp_enqueue_style('${slugifyTheme(intake.companyName)}-fonts', 'https://fonts.googleapis.com/css2?family=Newsreader:wght@500;700&family=Space+Grotesk:wght@400;500;700&display=swap');
}
add_action('wp_enqueue_scripts', '${sanitizePhpFunction(intake.companyName)}_enqueue_styles');

// Register menus
register_nav_menus(array(
    'primary' => __('Primary Menu', '${slugifyTheme(intake.companyName)}'),
    'footer' => __('Footer Menu', '${slugifyTheme(intake.companyName)}'),
));

// Add custom widgets
function ${sanitizePhpFunction(intake.companyName)}_widgets_init() {
    register_sidebar(array(
        'name' => __('Sidebar', '${slugifyTheme(intake.companyName)}'),
        'id' => 'sidebar',
        'description' => __('Main Sidebar', '${slugifyTheme(intake.companyName)}'),
        'before_widget' => '<div id="%1\\$s" class="widget %2\\$s">',
        'after_widget' => '</div>',
        'before_title' => '<h3 class="widget-title">',
        'after_title' => '</h3>',
    ));
}
add_action('widgets_init', '${sanitizePhpFunction(intake.companyName)}_widgets_init');

// Add custom post types
function ${sanitizePhpFunction(intake.companyName)}_register_post_types() {
    register_post_type('service', array(
        'public' => true,
        'label' => 'Services',
        'supports' => array('title', 'editor', 'thumbnail'),
    ));
}
add_action('init', '${sanitizePhpFunction(intake.companyName)}_register_post_types');
?>`;
}

